package poketmon;

public class Poketmon {

	String name;
	String color;
	String skill;
	
	public Poketmon(String name, String color) {
		this.name = name;
		this.color = color;
	}

	public Poketmon(String name, String color, String skill) {
		this.name = name;
		this.color = color;
		this.skill = skill;
	}
}
